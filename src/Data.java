    public String isInAnyAl(String toFind) { // encoding: first number indicates which al the rest indicates the index | if it beginns with 0 it doesn't exist in any list
        int index = findInAl(alCharacters, toFind);
        if(index != -1) {
            return "1" + index;
        }
        
        index = findInAl(alBows, toFind);
        if(index != -1) {
            return "2" + index;
        }
        
        index = findInAl(alCatalysts, toFind);
        if(index != -1) {
            return "3" + index;
        }
        
        index = findInAl(alClaymores, toFind);
        if(index != -1) {
            return "4" + index;
        }
        
        index = findInAl(alPolearms, toFind);
        if(index != -1) {
            return "5" + index;
        }
        
        index = findInAl(alSwords, toFind);
        if(index != -1) {
            return "6" + index;
        }
        
        return "0";
    }
    
    public String getAlNameFromNumber(int n) {
        if(n == 1) return "Character";
        if(n == 2) return "Bow";
        if(n == 3) return "Catalyst";
        if(n == 4) return "Claymore";
        if(n == 5) return "Polearm";
        if(n == 6) return "Sword";
        
        return "Other";
    }
    
    public int getNumberFromAlName(String name) {
        if(name.equals("Character")) return 1;
        if(name.equals("Bow")) return 2;
        if(name.equals("Catalyst")) return 3;
        if(name.equals("Claymore")) return 4;
        if(name.equals("Polearm")) return 5;
        if(name.equals("Sword")) return 6;
        
        return 0;
    }
    
    private int findInAl(ArrayList<String> al, String toFind) {
        for(int i = 0; i < al.size(); i++) {
            String toCompare = al.get(i).toLowerCase().replaceAll("\\s", "");
            if(toCompare.equals(toFind)) {
                return i;
            }
        }
        
        return -1;
    }
